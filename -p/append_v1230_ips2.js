lines.push('})')
lines.push('')
lines.push("ipcMain.handle('voiceprint:profileDelete', async (event, payload) => {")
lines.push('  try { return speakerProfile.deleteProfile(payload.id) }')
lines.push('  catch (e) { return { success: false, error: e.message } }')
lines.push('})')
lines.push('')
lines.push("ipcMain.handle('voiceprint:profileStats', async () => {")
