lines.push('  }')
lines.push('})')
lines.push('')
lines.push('// v1.23.0: 從獨立短音檔建立 profile')
lines.push("ipcMain.handle('voiceprint:profileBuildFromAudioFile', async (event, payload) => {")
lines.push('  const { audioPath, name, modelKey } = payload')
lines.push('  appLog("INFO", "voiceprint", `buildProfileFromAudioFile: ${audioPath} name=${name} modelKey=${modelKey}`)')
lines.push('  try {')
lines.push('    const p = await voiceprint.buildProfileFromAudioFile(audioPath, name, modelKey)')
lines.push('    const r = speakerProfile.saveProfile(p)')
lines.push('    if (r.success) return { success: true, profile: { ...p, id: r.id }, id: r.id }')
