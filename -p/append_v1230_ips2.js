lines.push('')
lines.push('// v1.23.0: 從 seeds 建立 profile')
lines.push("ipcMain.handle('voiceprint:profileBuildFromSeeds', async (event, payload) => {")
lines.push('  const { audioPath, segments, seeds, modelKey } = payload')
lines.push('  appLog("INFO", "voiceprint", `buildProfile: ${audioPath} (${seeds.length} seeds, modelKey=${modelKey})`)')
