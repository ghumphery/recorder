lines.push('    appLog("INFO", "voiceprint", `建立 ${savedIds.length}/${profiles.length} 個 profile`)')
lines.push('    return { success: true, profiles, savedIds, count: savedIds.length }')
lines.push('  } catch (e) {')
lines.push('    appLog("ERROR", "voiceprint", `buildProfile 失敗: ${e.message}`)')
